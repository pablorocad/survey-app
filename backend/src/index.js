//======================================================================================
//Imports===============================================================================
//======================================================================================
const express = require('express');
const surveyRouter = require('./routes/SurveyRoutes');

//======================================================================================
//Configuration=========================================================================
//======================================================================================
const app = express();
const PORT = 3000;

app.set('port',PORT);

//======================================================================================
//Endpoints=============================================================================
//======================================================================================

app.get('/',(req,res) => {
    res.send('Server Running');
})

//Survey endpoints
app.use(surveyRouter);

//======================================================================================
//Listen================================================================================
//======================================================================================
app.listen(PORT, () => {
    console.log(`Survey app listening on port: ${app.get('port')}`);
});